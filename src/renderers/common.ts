/**
 * Renders category, since, deprecated, table mapping, and source path metadata as a single line.
 */
export function renderMetadata(
  documentMeta: DocumentMeta,
  sourcePath: string | undefined,
  extra?: { dbSchema?: string; mappedTable?: string },
): string[] {
  const parts: string[] = [];
  if (documentMeta.category) {
    parts.push(`**Category:** ${documentMeta.category}`);
  }

  if (documentMeta.since) {
    parts.push(`**Since:** ${documentMeta.since}`);
  }

  if (documentMeta.deprecated) {
    parts.push(`**Deprecated:** ${documentMeta.deprecated}`);
  }

  if (extra?.mappedTable) {
    parts.push(`**Table:** \`${extra.mappedTable}\``);
  }

  if (extra?.dbSchema) {
    parts.push(`**Schema:** \`${extra.dbSchema}\``);
  }

  if (sourcePath) {
    parts.push(`**Defined in:** \`${sourcePath}\``);
  }

  if (parts.length === 0) {
    return [];
  }

  return [parts.join(' · '), ''];
}

/**
 * Renders a 6-column fields table (no Source column). Used by type-page and view-page.
 */
export function renderSimpleFieldsTable(fields: DataField[]): string[] {
  if (fields.length === 0) {
    return [];
  }

  const lines = [
    ...sectionHeading('Fields'),
    '',
    '| Field | Type | Required | Default | Attributes | Description |',
    '| --- | --- | --- | --- | --- | --- |',
  ];
  for (const field of fields) {
    const fieldDescription = stripCommentPrefix(field.comments) || '—';
    const fieldAnchor = `<a id="field-${field.name}"></a>`;
    lines.push(
      `| ${fieldAnchor}\`${field.name}\` | ${getFieldTypeName(field, false)} | ${isFieldRequired(field) ? 'Yes' : 'No'} | ${getDefaultValue(field)} | ${getFieldAttributes(field)} | ${fieldDescription} |`,
    );
  }

  lines.push('');
  return lines;
}

/**
 * Renders "Defined in" line + collapsible declaration block. Used by enum, type, and procedure pages.
 */
export function renderSourceAndDeclaration(
  node: Parameters<typeof getRelativeSourcePath>[0] & {
    $cstNode?: { text?: string };
  },
  schemaDir: string | undefined,
): string[] {
  const sourcePath = getRelativeSourcePath(node, schemaDir);
  const lines: string[] = [];
  if (sourcePath) {
    lines.push(`**Defined in:** \`${sourcePath}\``, '');
  }

  lines.push(...declarationBlock(node.$cstNode?.text, sourcePath));
  return lines;
}
