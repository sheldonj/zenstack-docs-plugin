/**
 * Renders an Index / Category / Entity breadcrumb trail.
 */
export function breadcrumbs(
  entityType: 'Enums' | 'Models' | 'Procedures' | 'Types' | 'Views',
  entityName: string,
  prefix: string,
): string {
  const anchor = entityType.toLowerCase();
  return `[Index](${prefix}index.md) / [${entityType}](${prefix}index.md#${anchor}) / \`${entityName}\``;
}

/**
 * Renders the auto-generated header banner with optional source file and generation date.
 */
export function generatedHeader(
  context?: GenerationContext,
  includeBanner = true,
): string[] {
  if (!includeBanner) {
    return [];
  }

  const metaParts: string[] = [];
  if (context?.schemaFile) {
    metaParts.push(`Source: ${context.schemaFile}`);
  }

  if (context) {
    metaParts.push(`Generated: ${context.generatedAt}`);
  }

  const metaLine = metaParts.length > 0 ? metaParts.join(' · ') : '';

  const contentLines = [
    'DO NOT MODIFY THIS FILE',
    'This file is automatically generated by ZenStack CLI and should not be manually updated.',
  ];
  if (metaLine) {
    contentLines.push(metaLine);
  }

  const maxLength = Math.max(...contentLines.map((l) => l.length));
  const border = '/'.repeat(maxLength + 6);
  const padded = contentLines.map((l) => `// ${l.padEnd(maxLength)} //`);

  return ['```', border, ...padded, border, '```', ''];
}

/**
 * Renders previous/next navigation links at the bottom of entity pages.
 */
export function navigationFooter(nav: Navigation | undefined): string[] {
  if (!nav) {
    return [];
  }

  const parts: string[] = [];
  if (nav.prev) {
    parts.push(`Previous: [${nav.prev.name}](${nav.prev.path})`);
  }

  if (nav.next) {
    parts.push(`Next: [${nav.next.name}](${nav.next.path})`);
  }

  if (parts.length === 0) {
    return [];
  }

  return ['---', '', parts.join(' · '), ''];
}

/**
 * Returns a level-2 heading with an inline anchor, emoji prefix, and optional description for the given section name.
 */
export function sectionHeading(name: string): string[] {
  const meta = SECTION_META[name];
  const emoji = meta?.emoji ?? '';
  const anchor = name.toLowerCase().replaceAll(/\s+/gu, '-');
  const prefix = emoji ? `${emoji} ` : '';
  const lines = [`<a id="${anchor}"></a>`, '', `## ${prefix}${name}`];
  if (meta?.description) {
    lines.push('', `> ${meta.description}`);
  }

  return lines;
}

const ZENSTACK_DOCS_BASE = 'https://zenstack.dev/docs/reference/zmodel';

const ENTITY_DOC_PATHS: Record<string, string> = {
  enum: `${ZENSTACK_DOCS_BASE}/enum`,
  model: `${ZENSTACK_DOCS_BASE}/model`,
  procedure: 'https://zenstack.dev/docs/category/reference',
  type: `${ZENSTACK_DOCS_BASE}/type`,
  view: `${ZENSTACK_DOCS_BASE}/view`,
};

/**
 * Builds a `Map<name, Navigation>` with prev/next links for a sorted list of entity names.
 */
export function buildNavList(
  sortedNames: string[],
  pathPrefix: string,
): Map<string, Navigation> {
  const navMap = new Map<string, Navigation>();
  for (let index = 0; index < sortedNames.length; index++) {
    const current = sortedNames[index]!;
    const nav: Navigation = {};
    if (index > 0) {
      const previous = sortedNames[index - 1]!;
      nav.prev = { name: previous, path: `${pathPrefix}${previous}.md` };
    }

    if (index < sortedNames.length - 1) {
      const next = sortedNames[index + 1]!;
      nav.next = { name: next, path: `${pathPrefix}${next}.md` };
    }

    navMap.set(current, nav);
  }

  return navMap;
}

/**
 * Renders a collapsible `<details>` block containing the raw ZModel declaration and optional source path.
 */
export function declarationBlock(
  cstText: string | undefined,
  sourcePath: string | undefined,
): string[] {
  if (!cstText) {
    return [];
  }

  const stripped = cstText
    .split('\n')
    .filter((line) => !line.trim().startsWith('///'))
    .join('\n');
  const summaryLabel = sourcePath
    ? `Declaration · <code>${sourcePath}</code>`
    : 'Declaration';
  return [
    '<details>',
    `<summary>${summaryLabel}</summary>`,
    '',
    '```prisma',
    stripped,
    '```',
    '',
    '</details>',
    '',
  ];
}

/**
 * Returns fields in declaration or alphabetical order.
 */
export function orderFields(
  fields: DataField[],
  order: RenderOptions['fieldOrder'],
): DataField[] {
  return order === 'alphabetical'
    ? [...fields].sort((a, b) => a.name.localeCompare(b.name))
    : [...fields];
}

/**
 * Renders a "References" section linking to the official ZenStack documentation for the entity type.
 */
export function referencesSection(
  entityType: 'enum' | 'model' | 'procedure' | 'type' | 'view',
): string[] {
  const url = ENTITY_DOC_PATHS[entityType];
  if (!url) {
    return [];
  }

  return [
    '---',
    '',
    ...sectionHeading('References'),
    '',
    `- [ZModel ${entityType}s — ZenStack documentation](${url})`,
    '',
  ];
}

/**
 * Renders a blockquoted description from doc-comments.
 */
export function renderDescription(comments: string[]): string[] {
  const description = stripCommentPrefix(comments);
  if (!description) {
    return [];
  }

  const lines: string[] = [];
  for (const line of description.split('\n')) {
    lines.push(`> ${line}`);
  }

  lines.push('');
  return lines;
}

